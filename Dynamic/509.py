# 509、斐波那契数
# 题⽬：斐波那契数，通常⽤ F(n) 表⽰，形成的序列称为 斐波那契数列 。该数列由 0 和 1 开始，后⾯的每⼀项数字都是前⾯两项数字的和。也就是：F(0) = 0，F(1) = 1F(n) = F(n - 1) + F(n - 2)，其中 n > 1给你n ，请计算 F(n) 。
# ⽰例 1：
# 输⼊：2
# 输出：1
# 解释：F(2) = F(1) + F(0) = 1 + 0 = 1
# ⽰例 2：
# 输⼊：3
# 输出：2解释：F(3) = F(2) + F(1) = 1 + 1 = 2
# ⽰例 3：
# 输⼊：4
# 输出：3
# 解释：F(4) = F(3) + F(2) = 2 + 1 = 3
# 提⽰：
# 0 <= n <= 30
