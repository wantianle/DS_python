# 763、划分字母区间
# 题⽬：字符串 S 由⼩写字母组成。我们要把这个字符串划分为尽可能多的⽚段，同⼀字母最多出现在⼀个⽚段中。返回⼀个表⽰每个字符串⽚段的长度的列表。
# ⽰例：
# 输⼊：S = "ababcbacadefegdehijhklij"
# 输出：[9,7,8]
# 解释：划分结果为 "ababcbaca", "defegde", "hijhklij"。每个字母最多出现在⼀个⽚段中。像 "ababcbacadefegde", "hijhklij" 的划分是错误的，因为划分的⽚段数较少。
# 提⽰：
# S的长度在[1, 500]之间。
# S只包含⼩写字母 'a' 到 'z' 。
